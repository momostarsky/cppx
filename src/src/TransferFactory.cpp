DicomUID TransferFactory::GetDicomUID(std::string &idstr) {
    auto iter = _uids.find(idstr);
    if (iter == _uids.end()) {
        return DicomUID::Empty;
    }
    return iter->second;  //second返回iter的value
}

DicomTransferSyntax TransferFactory::GetTransferSyntax(DicomUID &tsuid) {
    auto iter = _tsmaps.find(tsuid.uid);
    if (iter == _tsmaps.end()) {
        return DicomTransferSyntax{
                .UID = DicomUID::Empty,
        };
    }
    return iter->second;  //second返回iter的value
}
