    double Unity::add_numbers(const double f1, const double f2)
    {
        return f1 + f2;
    }

    double Unity::subtract_numbers(const double f1, const double f2)
    {
        return f1 - f2;
    }

    double Unity::multiply_numbers(const double f1, const double f2)
    {
        return f1 * f2;
    }

